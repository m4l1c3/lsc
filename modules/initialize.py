"""
Initialization modules
"""

class Initialization(object):
    """
    Initialization object
    """

    def __init__(self):
        return

    def initialize(self):
        """
        setup module
        """
        return self

    @staticmethod
    def check_for_config(config_name):
        """
        check for existing config of name
        """
        return config_name

    @staticmethod
    def write_config(config):
        """
        write newly setup options to a config file
        """
        return config
